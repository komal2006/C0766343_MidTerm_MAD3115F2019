import Foundation
extension Int
{
    func Min() -> String
    {
        return "\(self) Minutes"
    }
    
    func GB() -> String
    {
        return "\(self) GB"
    }
    
    
    func Units() -> String
    {
        return "\(self) Units"
    }
    
}
